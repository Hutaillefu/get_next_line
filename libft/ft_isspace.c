#include "libft.h"

int		ft_isspace(int c)
{
	unsigned char c1;

	c1 = (unsigned char)c;
	if (ft_isblank(c) == 1 || c1 == '\n' || c1 == '\f' || c1 == '\v'
			|| c1 == '\r')
		return (1);
	return (0);
}
