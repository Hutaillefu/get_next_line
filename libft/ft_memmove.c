#include "libft.h"

void	*ft_memmove(void *dst, const void *src, size_t n)
{
	unsigned char	*dst1;
	unsigned char	*src1;

	dst1 = (unsigned char *)dst;
	src1 = (unsigned char *)src;
	if (src1 < dst1)
	{
		while (n > 0)
		{
			n--;
			dst1[n] = src1[n];
		}
	}
	else
		ft_memcpy(dst, src, n);
	return (dst);
}
